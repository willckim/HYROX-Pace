/**
 * Plan Screen  
 * Shows segment-by-segment race day plan - the thing people screenshot
 */

import React from 'react';
import { 
  View, 
  Text, 
  StyleSheet, 
  SafeAreaView, 
  ScrollView,
  TouchableOpacity,
  Share,
} from 'react-native';
import { NativeStackNavigationProp } from '@react-navigation/native-stack';
import { RouteProp } from '@react-navigation/native';
import { HomeStackParamList } from '../navigation/TabNavigator';
import { getRiskColor } from '../lib/api';
import { SegmentPlan } from '../types';

type PlanScreenProps = {
  navigation: NativeStackNavigationProp<HomeStackParamList, 'Plan'>;
  route: RouteProp<HomeStackParamList, 'Plan'>;
};

const SEGMENT_NAMES: Record<string, string> = {
  'RUN': 'Run',
  'SKI_ERG': 'SkiErg',
  'SLED_PUSH': 'Sled Push',
  'SLED_PULL': 'Sled Pull',
  'BURPEE_BROAD_JUMP': 'Burpee BJ',
  'ROW': 'Row',
  'FARMERS_CARRY': 'Farmers',
  'SANDBAG_LUNGES': 'Lunges',
  'WALL_BALLS': 'Wall Balls',
};

export default function PlanScreen({ navigation, route }: PlanScreenProps) {
  const { simulation } = route.params;
  const { segment_plans, predictions } = simulation;

  let runCount = 0;

  const getSegmentDisplayName = (segment: SegmentPlan): string => {
    if (segment.segment_type === 'RUN') {
      runCount++;
      return `Run ${runCount}`;
    }
    runCount = Math.floor(segment.segment_order / 2); // Reset counter for display
    return SEGMENT_NAMES[segment.segment_type] || segment.segment_type;
  };

  const handleShare = async () => {
    try {
      let planText = `üèÉ HYROXPace Race Plan\n`;
      planText += `‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ\n`;
      planText += `Target: ${predictions.likely_display}\n\n`;
      
      let rNum = 0;
      segment_plans.forEach((seg) => {
        const name = seg.segment_type === 'RUN' ? `Run ${++rNum}` : SEGMENT_NAMES[seg.segment_type];
        const pace = seg.target_pace_per_km ? ` (${seg.target_pace_per_km})` : '';
        planText += `${name}: ${seg.target_time_display}${pace}\n`;
        planText += `  ‚Üí ${seg.execution_cue}\n\n`;
      });
      
      planText += `\nGenerated by HYROXPace`;
      
      await Share.share({
        message: planText,
      });
    } catch (error) {
      console.error(error);
    }
  };

  // Reset run counter for render
  runCount = 0;

  return (
    <SafeAreaView style={styles.container}>
      {/* Header with target time */}
      <View style={styles.header}>
        <Text style={styles.headerLabel}>Target Finish</Text>
        <Text style={styles.headerTime}>{predictions.likely_display}</Text>
      </View>

      <ScrollView style={styles.scrollView}>
        {/* Segment Table */}
        <View style={styles.table}>
          {/* Table Header */}
          <View style={styles.tableHeader}>
            <Text style={[styles.headerCell, styles.segmentCol]}>Segment</Text>
            <Text style={[styles.headerCell, styles.timeCol]}>Target</Text>
            <Text style={[styles.headerCell, styles.effortCol]}>Effort</Text>
          </View>

          {/* Table Rows */}
          {segment_plans.map((segment, idx) => {
            const isRun = segment.segment_type === 'RUN';
            const riskColor = getRiskColor(segment.risk_level);
            
            return (
              <View 
                key={segment.segment_order} 
                style={[
                  styles.tableRow,
                  idx % 2 === 0 && styles.tableRowAlt,
                  segment.risk_level === 'high' && styles.tableRowHighRisk,
                ]}
              >
                {/* Segment Name */}
                <View style={styles.segmentCol}>
                  <View style={styles.segmentNameRow}>
                    <View style={[styles.riskDot, { backgroundColor: riskColor }]} />
                    <Text style={[styles.segmentName, isRun && styles.segmentNameRun]}>
                      {getSegmentDisplayName(segment)}
                    </Text>
                  </View>
                  {segment.target_pace_per_km && (
                    <Text style={styles.paceText}>{segment.target_pace_per_km}</Text>
                  )}
                </View>

                {/* Target Time */}
                <View style={styles.timeCol}>
                  <Text style={styles.timeText}>{segment.target_time_display}</Text>
                </View>

                {/* Effort */}
                <View style={styles.effortCol}>
                  <View style={styles.effortBarContainer}>
                    <View 
                      style={[
                        styles.effortBar, 
                        { 
                          width: `${segment.effort_percentage}%`,
                          backgroundColor: segment.effort_percentage > 85 ? '#ef4444' : 
                                          segment.effort_percentage > 75 ? '#f97316' : '#22c55e'
                        }
                      ]} 
                    />
                  </View>
                  <Text style={styles.effortText}>{segment.effort_percentage}%</Text>
                </View>
              </View>
            );
          })}
        </View>

        {/* Execution Cues */}
        <View style={styles.cuesSection}>
          <Text style={styles.sectionTitle}>Execution Cues</Text>
          {segment_plans.map((segment) => {
            const isHighRisk = segment.risk_level === 'high' || segment.risk_level === 'critical';
            let displayRunCount = 0;
            
            return (
              <View 
                key={`cue-${segment.segment_order}`}
                style={[styles.cueCard, isHighRisk && styles.cueCardHighRisk]}
              >
                <View style={styles.cueHeader}>
                  <Text style={styles.cueName}>
                    {segment.segment_type === 'RUN' 
                      ? `Run ${Math.ceil(segment.segment_order / 2)}`
                      : SEGMENT_NAMES[segment.segment_type]}
                  </Text>
                  {isHighRisk && (
                    <View style={styles.riskBadge}>
                      <Text style={styles.riskBadgeText}>‚ö†Ô∏è High Risk</Text>
                    </View>
                  )}
                </View>
                <Text style={styles.cueText}>{segment.execution_cue}</Text>
                {segment.risk_reason && (
                  <Text style={styles.riskReason}>{segment.risk_reason}</Text>
                )}
              </View>
            );
          })}
        </View>

        {/* Legend */}
        <View style={styles.legend}>
          <Text style={styles.legendTitle}>Risk Levels</Text>
          <View style={styles.legendRow}>
            <View style={styles.legendItem}>
              <View style={[styles.legendDot, { backgroundColor: '#22c55e' }]} />
              <Text style={styles.legendText}>Low</Text>
            </View>
            <View style={styles.legendItem}>
              <View style={[styles.legendDot, { backgroundColor: '#eab308' }]} />
              <Text style={styles.legendText}>Moderate</Text>
            </View>
            <View style={styles.legendItem}>
              <View style={[styles.legendDot, { backgroundColor: '#f97316' }]} />
              <Text style={styles.legendText}>High</Text>
            </View>
            <View style={styles.legendItem}>
              <View style={[styles.legendDot, { backgroundColor: '#ef4444' }]} />
              <Text style={styles.legendText}>Critical</Text>
            </View>
          </View>
        </View>

      </ScrollView>

      {/* Share Button */}
      <View style={styles.footer}>
        <TouchableOpacity style={styles.shareButton} onPress={handleShare}>
          <Text style={styles.shareButtonText}>üì§ Share Race Plan</Text>
        </TouchableOpacity>
      </View>
    </SafeAreaView>
  );
}

